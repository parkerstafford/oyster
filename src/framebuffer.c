void fb_init(struct limine_framebuffer *framebuffer) {
    fb = framebuffer;
    cursor_x = 0;
    cursor_y = 0;
    fg_color = 0x00ffffff;
}

void fb_set_color(uint32_t color) {
    fg_color = color;
}

void fb_putpixel(int x, int y, uint32_t color) {
    if (x < 0 || y < 0 || (uint64_t)x >= fb->width || (uint64_t)y >= fb->height) return;
    
    uint32_t *pixel = (uint32_t *)((uint8_t *)fb->address + y * fb->pitch + x * 4);
    *pixel = color;
}

void fb_clear(uint32_t color) {
    for (uint64_t y = 0; y < fb->height; y++) {
        for (uint64_t x = 0; x < fb->width; x++) {
            fb_putpixel(x, y, color);
        }
    }
    cursor_x = 0;
    cursor_y = 0;
}

static void fb_scroll(void) {
    uint64_t line_bytes = fb->pitch;
    uint8_t *framebuffer = (uint8_t *)fb->address;
    
    for (uint64_t y = FONT_HEIGHT; y < fb->height; y++) {
        uint8_t *dst = framebuffer + (y - FONT_HEIGHT) * line_bytes;
        uint8_t *src = framebuffer + y * line_bytes;
        for (uint64_t x = 0; x < line_bytes; x++) {
            dst[x] = src[x];
        }
    }
    
    for (uint64_t y = fb->height - FONT_HEIGHT; y < fb->height; y++) {
        for (uint64_t x = 0; x < fb->width; x++) {
            fb_putpixel(x, y, 0x001a1a2e);
        }
    }
}

void fb_putchar(char c) {
    if (c == '\n') {
        cursor_x = 0;
        cursor_y += FONT_HEIGHT;
        if ((uint64_t)(cursor_y + FONT_HEIGHT) > fb->height) {
            fb_scroll();
            cursor_y -= FONT_HEIGHT;
        }
        return;
    }
    
    if (c == '\r') {
        cursor_x = 0;
        return;
    }
    
    if (c == '\t') {
        cursor_x += FONT_WIDTH * 4;
        return;
    }
    
    if (c == '\b') {
        if (cursor_x >= FONT_WIDTH) {
            cursor_x -= FONT_WIDTH;
        }
        return;
    }
    
    if ((uint64_t)(cursor_x + FONT_WIDTH) > fb->width) {
        cursor_x = 0;
        cursor_y += FONT_HEIGHT;
        if ((uint64_t)(cursor_y + FONT_HEIGHT) > fb->height) {
            fb_scroll();
            cursor_y -= FONT_HEIGHT;
        }
    }
    
    unsigned char uc = (unsigned char)c;
    if (uc > 127) uc = '?';
    
    const uint8_t *glyph = font_8x16[uc];
    
    for (int row = 0; row < FONT_HEIGHT; row++) {
        for (int col = 0; col < FONT_WIDTH; col++) {
            if (glyph[row] & (0x80 >> col)) {
                fb_putpixel(cursor_x + col, cursor_y + row, fg_color);
            }
        }
    }
    
    cursor_x += FONT_WIDTH;
}

void fb_puts(const char *s) {
    while (*s) {
        fb_putchar(*s++);
    }
}

static void fb_print_int(int64_t n) {
    if (n < 0) {
        fb_putchar('-');
        n = -n;
    }
    if (n >= 10) {
        fb_print_int(n / 10);
    }
    fb_putchar('0' + (n % 10));
}

static void fb_print_uint(uint64_t n) {
    if (n >= 10) {
        fb_print_uint(n / 10);
    }
    fb_putchar('0' + (n % 10));
}

static void fb_print_hex(uint64_t n) {
    const char *hex = "0123456789abcdef";
    if (n >= 16) {
        fb_print_hex(n / 16);
    }
    fb_putchar(hex[n % 16]);
}

void fb_printf(const char *fmt, ...) {
    va_list args;
    va_start(args, fmt);
    
    while (*fmt) {
        if (*fmt == '%') {
            fmt++;
            switch (*fmt) {
                case 'd':
                case 'i':
                    fb_print_int(va_arg(args, int));
                    break;
                case 'u':
                    fb_print_uint(va_arg(args, unsigned int));
                    break;
                case 'l':
                    fmt++;
                    if (*fmt == 'd') {
                        fb_print_int(va_arg(args, int64_t));
                    } else if (*fmt == 'u') {
                        fb_print_uint(va_arg(args, uint64_t));
                    } else if (*fmt == 'x') {
                        fb_print_hex(va_arg(args, uint64_t));
                    }
                    break;
                case 'x':
                    fb_print_hex(va_arg(args, unsigned int));
                    break;
                case 'p':
                    fb_puts("0x");
                    fb_print_hex(va_arg(args, uint64_t));
                    break;
                case 's':
                    fb_puts(va_arg(args, const char *));
                    break;
                case 'c':
                    fb_putchar((char)va_arg(args, int));
                    break;
                case '%':
                    fb_putchar('%');
                    break;
                default:
                    fb_putchar('%');
                    fb_putchar(*fmt);
                    break;
            }
        } else {
            fb_putchar(*fmt);
        }
        fmt++;
    }
    
    va_end(args);
}
